    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        int posicion,dato1;
        String  dato2;
        posicion=tblListaVehiculos.getSelectedRow();

        if(posicion>=0){
            dato1=Integer.parseInt(JOptionPane.showInputDialog("Campo a modificar: Tipo (1), "
                + "Marca(2), Modelo(3), Placa(4),  Descripcion(5)"));
        dato2=JOptionPane.showInputDialog("Digite el dato a modificar");
        modelov.setValueAt(dato2, posicion, dato1);
        }
    }//GEN-LAST:event_ModificarActionPerformed

